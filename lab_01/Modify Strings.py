#Upper Case
a = "Hello, World!"
print(a.upper())
#Output: HELLO, WORLD!


#Lower Case
a = "Hello, World!"
print(a.lower())
#Output: hello, world!


#Remove Whitespace
a = " Hello, World! "
print(a.strip()) 
#Output: "Hello, World!"


#Replace String
a = "Hello, World!"
print(a.replace("H", "J"))
#Output: Jello, World!


#Split String   
a = "Hello, World!"
print(a.split(","))
#Output: ['Hello', ' World!']